  // Creează butoanele pentru categorii
  window.onload = function () {
    const container = document.getElementById("category-container");
  
    Object.keys(DATA).forEach(cat => {
      const btn = document.createElement("button");
      btn.className = "button";
      btn.textContent = cat;
      btn.onclick = () => showSubcategories(cat);
      container.appendChild(btn);
    });
  };
  
  function showSubcategories(category) {
    const catDiv = document.getElementById("category-container");
    const subDiv = document.getElementById("subcategory-container");
    const title = document.getElementById("subcategory-title");
    const list = document.getElementById("subcategory-list");
  
    title.textContent = category;
    list.innerHTML = "";
  
    for (let key in DATA[category]) {
      const li = document.createElement("li");
      const a = document.createElement("a");
      a.href = DATA[category][key];
      a.textContent = key;
      a.target = "_blank";
      li.appendChild(a);
      list.appendChild(li);
    }
  
    catDiv.classList.add("hidden");
    subDiv.classList.remove("hidden");
  }
  
  function goBack() {
    document.getElementById("subcategory-container").classList.add("hidden");
    document.getElementById("category-container").classList.remove("hidden");
  }
  